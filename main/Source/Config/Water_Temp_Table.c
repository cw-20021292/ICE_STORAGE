/// @brief      온도값 Char형 변환 함수
/// @details    온도를 Char 형태로 변환해서 반환한다
/// @param      mf32temp : 측정된 온도 데이터(소수점 있는 데이터)
/// @return     mf32Return : Char형으로 변환한 온도(소수점 부분은 삭제)
U8 ConvTemp2Char(F32 mf32temp)
{
    I16 mi16Cal = 0;
    U8 mu8Return = 0;

    mi16Cal = (I16)(mf32temp * 10.0f);  // 10을 곱해서 소수점을 일의 자리로 만듬
    mi16Cal -= mi16Cal % 10;            // 1의 자리를 0으로 만들어 줌
    mi16Cal = mi16Cal / 10;             // 10을 다시 나눠서 소수점 없는 온도로 만들어줌
    mu8Return = (U8)(mi16Cal);

    return  mu8Return;
}


