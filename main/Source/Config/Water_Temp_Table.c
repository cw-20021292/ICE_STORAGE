/// @brief      온도 센서의 섭씨 온도 값을 확인하는 함수
/// @details    ID에 해당하는 온도 센서의 섭씨 온도값을 반환한다
/// @param      mu8ID : 온도 값을 확인하려는 온도 센서의 ADC ID
/// @return     mf32Return : 변경된 섭씨값(소수점 포함)
F32 Get_Temp(U8 mu8ID)
{
    U16 mu16AD_DataBuf = 0;
    F32 mf32Return = 0.0f;

    mu16AD_DataBuf = Get_ADC_Data(mu8ID);       // ID에 해당하는 ADC값 확인

    switch (mu8ID)
    {
#if ( (CONFIG_TEMP_TABLE_COLD_JAMESTECH == USE) || (CONFIG_TEMP_TABLE_COLD_RATTRON == USE) )
        case COOL_THERMISTOR_1_ID:
#if (COOL_THERMISTOR_COUNT == 2)
        case COOL_THERMISTOR_2_ID:
#endif
            mf32Return = ConvAdc2Temp_ColdWater(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_ROOM_JAMESTECH == USE)
        case ROOM_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_Room(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_HOT_TANK_JAMESTECH == USE)
        case HOT_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_HotTank(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_HOT_IN_SK == USE)
        case HOT_IN_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_HotIn(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_HOT_BODY_SK == USE)
        case HOT_BODY_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_HotBody(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_HOT_OUT_SK == USE)
        case HOT_OUT_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_HotOut(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_HEATSINK_JAMESTECH == USE)
        case TEM_HEATSINK_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_Heatsink(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_AIR_JAMESTECH == USE)
        case AIR_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_Air(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_FAUCET_OUT_SK == USE)
        case FAUCET_OUT_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_FaucetOut(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_TDS_IN_SK == USE)
        case TDS_IN_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_TdsIn(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_TDS_OUT_SK == USE)
        case TDS_OUT_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_TdsOut(mu16AD_DataBuf);
            break;
#endif

#if (CONFIG_TEMP_TABLE_EVA == USE)
        case EVA_THERMISTOR_ID:
            mf32Return = ConvAdc2Temp_Eva(mu16AD_DataBuf);
            break;
#endif

        default:
            mf32Return = 0.0f;
            break;
    }

    return  mf32Return;
}


