/**
 * @brief             Function For Conversion Integer to Temp
 * @detail            none
 * @param     temp    none
 * @return            none
**/
static TEMP_T ConvInt2Temp(int temp)
{
    TEMP_T  retTemp = 0.0f;

    retTemp = (TEMP_T)(temp/10);
    retTemp += (TEMP_T)(temp%10) * 0.1f;

    return retTemp;
}

